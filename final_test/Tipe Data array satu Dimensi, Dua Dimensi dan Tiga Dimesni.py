# Array / List 1 Dimensi
array1 = [10, 20, 30, 40, 50]

print("Array 1 Dimensi:")
print(array1)


# Array / List 2 Dimensi (matriks)
array2 = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

print("\nArray 2 Dimensi:")
for row in array2:
    print(row)


# Array / List 3 Dimensi (kubus)
array3 = [
    [
        [1, 2], 
        [3, 4]
    ],
    [
        [5, 6],
        [7, 8]
    ]
]

print("\nArray 3 Dimensi:")
for layer in array3:
    print(layer)

